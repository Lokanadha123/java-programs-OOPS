 public class poly1 {
        static int sub(int a, int b) {
            int c = a + b;
            return c;
        }

        static double sub(double a, double b, double c) {
            double d = a + b + c;
            return d;
        }

        public static void main(String[] args) {
            System.out.println(poly1.sub(10,12));
            System.out.println(poly1.sub(1200,1400,1600));


    }

}
